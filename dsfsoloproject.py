#Question 1: Which publishers dominate the video game market based on global sales?

#Data Prep
publisher_stats = df.groupby('Publisher').agg({
    'Global_Sales': 'sum',
    'Name': 'count'
}).rename(columns={'Name': 'Game_Count'}).reset_index()

publisher_stats['Avg_Sales'] = publisher_stats['Global_Sales'] / publisher_stats['Game_Count']

#Sort and take top 10
publisher_stats = publisher_stats.sort_values(by='Global_Sales', ascending=False)
top_publishers = publisher_stats.head(10)

#Plotting
plt.figure(figsize=(12,6))

scatter = plt.scatter(
    top_publishers['Game_Count'],
    top_publishers['Global_Sales'],
    s=top_publishers['Avg_Sales'] * 200,   # bigger bubbles
    c=top_publishers['Avg_Sales'],         # color = efficiency
    cmap='viridis',
    alpha=0.7
)

#Add colorbar
plt.colorbar(scatter, label='Average Sales per Game')

#Label each point
for i, row in top_publishers.iterrows():
    plt.text(
        row['Game_Count'] + 5,
        row['Global_Sales'] + 5,
        row['Publisher'],
        fontsize=9
    )


# Labelling axis
plt.xlabel('Number of Games Released')
plt.ylabel('Total Global Sales (Millions)')
plt.title('Top Publishers: Output vs Total Sales (Size & Color = Avg Sales per Game)')
