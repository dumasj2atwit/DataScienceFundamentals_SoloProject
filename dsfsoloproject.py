# Question 2: How has the number of game releases changed over time and what genres are more prevalent in those years?

from sklearn.preprocessing import PolynomialFeatures
from sklearn.linear_model import LinearRegression
from sklearn.metrics import r2_score

#Clean and Filter Data
year_genre = df[['Year', 'Genre']].dropna()
year_genre['Year'] = year_genre['Year'].astype(int)

#Cut off after 2015
year_genre = year_genre[year_genre['Year'] <= 2015]

#Count releases
year_genre_counts = year_genre.groupby(['Year', 'Genre']).size().reset_index(name='Count')

#Pivot
genre_pivot = year_genre_counts.pivot(index='Year', columns='Genre', values='Count').fillna(0)
#Had to figure out how to use multiple colors using AI here
#Custom Colors
colors = plt.cm.tab20.colors  # 20 distinct colors

# Plot
genre_pivot.plot(
    kind='area',
    figsize=(14, 7),
    stacked=True,
    color=colors
)

plt.xlabel("Year")
plt.ylabel("Number of Game Releases")
plt.title("Video Game Releases by Genre Over Time (Up to 2015)")
plt.legend(title="Genre", bbox_to_anchor=(1.2, 1), loc='upper left')
plt.grid(True)
plt.show()


# Filter early years
early_data = genre_pivot[genre_pivot.index <= 1995]

# Plot
early_data.plot(
    kind='area',
    figsize=(14, 7),
    stacked=True,
    color=colors
)

plt.xlabel("Year")
plt.ylabel("Number of Game Releases")
plt.title("Video Game Releases by Genre (Early Years: Pre-1995)")
