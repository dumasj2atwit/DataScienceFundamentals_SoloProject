# ML:Polynomial regression on total yearly releases

# Count total game releases per year
games_per_year = year_genre.groupby('Year').size().reset_index(name='Game_Count')

# Set up X and y
X = games_per_year[['Year']]
y = games_per_year['Game_Count']

# Polynomial features
poly = PolynomialFeatures(degree=3)
X_poly = poly.fit_transform(X)

# Train model
model = LinearRegression()
model.fit(X_poly, y)

# Predict
y_pred = model.predict(X_poly)

# R^2 score
r2 = r2_score(y, y_pred)

# Plot actual vs polynomial trend
plt.figure(figsize=(12, 6))
plt.scatter(games_per_year['Year'], games_per_year['Game_Count'], label='Actual Releases')
plt.plot(games_per_year['Year'], y_pred, linewidth=3, label=f'Polynomial Regression (R² = {r2:.3f})')

plt.xlabel("Year")
plt.ylabel("Number of Game Releases")
plt.title("Polynomial Trend of Total Game Releases Over Time")
plt.legend()
plt.grid(True)
plt.show()